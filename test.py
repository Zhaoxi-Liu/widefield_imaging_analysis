def process_text(text):
    lines = text.split("\n")
    processed_lines = []
    for line in lines:
        if "=" in line:
            first_equal_index = line.find("=")
            second_equal_index = line.find("=", first_equal_index + 1)
            if second_equal_index != -1:
                processed_line = line[:second_equal_index]
                processed_lines.append(processed_line)
            else:
                processed_lines.append(line)
        else:
            processed_lines.append(line)
    return "\n".join(processed_lines)

